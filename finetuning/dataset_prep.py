class DatasetPreparator:
    """
    Prepares financial QA dataset for fine-tuning.

    The dataset format for Flan-T5:
        Input:  "question: {question} context: {context}"
        Target: "{answer}"

    WHY THIS FORMAT?
    Flan-T5 was pre-trained on instruction-following tasks
    using this exact input/output structure.
    Matching the pre-training format gives better results.
    """

    def __init__(self):
        self.cfg = get_config()
        self.output_dir = Path("data/finetuning")
        self.output_dir.mkdir(parents=True, exist_ok=True)
        log.info("DatasetPreparator initialized")

    def prepare_dataset(
        self,
        additional_chunks: list[dict] = None
    ) -> tuple[Dataset, Dataset]:
        """
        Prepare train and validation datasets.

        Args:
            additional_chunks: Optional chunks from ingested docs
                               to augment training data

        Returns:
            Tuple of (train_dataset, val_dataset)
        """
        examples = FINANCIAL_QA_EXAMPLES.copy()

        # If we have ingested chunks, create additional QA pairs
        if additional_chunks:
            chunk_examples = self._create_from_chunks(additional_chunks)
            examples.extend(chunk_examples)
            log.info(f"Added {len(chunk_examples)} examples from chunks")

        # Shuffle for randomness
        random.shuffle(examples)

        # Format for Flan-T5
        formatted = [self._format_example(e) for e in examples]

        # 80/20 train/validation split
        # WHY VALIDATION SET?
        # To detect overfitting — if train loss drops but
        # val loss rises, the model is memorizing not learning
        split_idx = int(len(formatted) * 0.8)
        train_data = formatted[:split_idx]
        val_data = formatted[split_idx:]

        train_dataset = Dataset.from_list(train_data)
        val_dataset = Dataset.from_list(val_data)

        # Save raw examples for inspection
        self._save_examples(examples)

        log.info(
            f"Dataset prepared | "
            f"train={len(train_data)} | "
            f"val={len(val_data)}"
        )

        return train_dataset, val_dataset

    def _format_example(self, example: dict) -> dict:
        """
        Format a QA example for Flan-T5 input/output.

        Flan-T5 expects:
            input_text:  "question: ... context: ..."
            target_text: "answer text here"
        """
        input_text = (
            f"question: {example['question']} "
            f"context: {example['context']}"
        )
        return {
            "input_text": input_text,
            "target_text": example["answer"]
        }

    def _create_from_chunks(
        self,
        chunks: list[dict],
        max_examples: int = 20
    ) -> list[dict]:
        """
        Auto-generate QA pairs from document chunks.

        This is a simple heuristic approach.
        In production you'd use a larger LLM to generate
        high-quality QA pairs from your documents.
        """
        examples = []
        financial_keywords = [
            "revenue", "net sales", "income", "expenses",
            "assets", "liabilities", "cash", "earnings"
        ]

        for chunk in chunks[:max_examples]:
            text = chunk.get("text", "")

            # Only use chunks with financial content
            if any(kw in text.lower() for kw in financial_keywords):
                examples.append({
                    "question": "What financial information is in this excerpt?",
                    "context": text[:300],
                    "answer": f"This excerpt contains: {text[:150]}"
                })

        return examples

    def _save_examples(self, examples: list[dict]):
        """Save dataset to disk for inspection."""
        output_file = self.output_dir / "training_examples.json"
        with open(output_file, "w") as f:
            json.dump(examples, f, indent=2)
        log.info(f"Saved training examples to {output_file}")